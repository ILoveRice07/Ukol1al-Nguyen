#Úkol 1-Výtvoření pole
array = ["měsíc", "den", "čtverec", "kláda", "sešit",]
print(array)

#Úkol 2-Přidání prvku vítr
array.append("vítr")
print(array)

#Úkol 3-Odstranění druhého prvku z pole
array.remove(array[1])
print(array)

#Úkol 4-Záměna páté hodnoty za "slunce"
array[4] = "slunce"
print(array)

#Úkol 5-Přídání dvou stringových hodnot
arrayB = ["kámen", "kruh"]
array.extend(arrayB)
print(array)

